
# Igual a: ==

print('a' == 'A')

print(ord('a'))
print(ord('A'))

print(2 == 2)
print(2 == 3)
print(2 == 2.0)
print(2 == 2.1)

print("thiago" == "Thiago")
print("Thiago" == "Thiago")

n1 = 10
n2 = 10

print(n1 == n2)

# Maior que: >

print(12 > 3)
print(1 > 3)

print('A' > 'a')
print('thiago' > 'Thiago')
print(len('thiago'))
print(len('Thiago'))

print(len('thiago') > len('Thiago'))

# Menor: <

print(9 < 10)
print(9 < 5)

n3 = 17
n4 = 20

print(n3 < n4)

# Diferente de: !=

print('a' != 'A')
print('abcd' != 'ABCD')
print(12 != 5)

print(5 != 5)

